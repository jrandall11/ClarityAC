package model;

/**
 *
 * @author joshrandall
 */
public class Aircraft {
    private int id;
    private String date;
    private String type;
    private String size;

    public Aircraft(int id, String date, String type, String size) {
        this.id = id;
        this.date = date;
        this.type = type;
        this.size = size;
    }
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
}
